function translatePage(lang) {
  document.querySelectorAll("[data-translate]").forEach((element) => {
    const key = element.getAttribute("data-translate");
    if (translations[lang] && translations[lang][key]) {
      element.textContent = translations[lang][key];
    }
  });
  document.documentElement.lang =
    lang === "en" ? "en-US" : lang === "es" ? "es-ES" : "pt-BR";
  document.title = translations[lang].title;
  localStorage.setItem("language", lang);
}

document.getElementById("language-selector").addEventListener("change", (e) => {
  translatePage(e.target.value);
});

// Load saved language or default to Portuguese
const savedLang = localStorage.getItem("language") || "pt";
document.getElementById("language-selector").value = savedLang;
translatePage(savedLang);

// Carousel functionality (unchanged)
const carousel = document.querySelector(".course-carousel");
const track = carousel.querySelector(".carousel-track");
const slides = Array.from(track.children);
const prevBtn = carousel.querySelector(".prev");
const nextBtn = carousel.querySelector(".next");
const slideWidth = slides[0].getBoundingClientRect().width + 32;

let currentIndex = 0;

const updateCarousel = () => {
  track.style.transform = `translateX(-${currentIndex * slideWidth}px)`;
};

const moveToNext = () => {
  if (currentIndex < slides.length - 3) {
    currentIndex++;
  } else {
    currentIndex = 0;
  }
  updateCarousel();
};

const moveToPrev = () => {
  if (currentIndex > 0) {
    currentIndex--;
  } else {
    currentIndex = slides.length - 3;
  }
  updateCarousel();
};

nextBtn.addEventListener("click", moveToNext);
prevBtn.addEventListener("click", moveToPrev);

let autoScroll = setInterval(moveToNext, 5000);

carousel.addEventListener("mouseenter", () => clearInterval(autoScroll));
carousel.addEventListener("mouseleave", () => {
  autoScroll = setInterval(moveToNext, 5000);
});
